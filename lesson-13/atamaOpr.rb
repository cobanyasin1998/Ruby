# = : Atama Operatörü
# Sağındaki değeri solundaki değere atar
x = 6 #x değişkenine 1 değerini atadık

# += : kendi değerini sağındaki değer ile toplar ve tekrar atama yapar
puts x += 2
# -= : kendi değerini sağındaki değer ile çıkartır ve tekrar atama yapar
puts x -= 2
# (*)= : kendi değerini sağındaki değer ile çarpar ve tekrar atama yapar
puts x *= 2
# /= : kendi değerini sağındaki değer ile böler ve tekrar atama yapar
puts x /= 2
# %= : kendi değerini sağındaki değer ile modunu alıp ve tekrar atama yapar
puts x %= 2

# (**)= : kendi değerini sağındaki değer ile üssünü alıp ve tekrar atama yapar
puts x **= 2